var pi = 3.14159265;
var half_pi = 1.57079632679;

print("=== PI ===");
print(Math.PI);

print("=== abs ===");
print(Math.abs());
print(Math.abs(5));
print(Math.abs(-2));

print("=== cos ===");
print(Math.cos());
print(Math.cos(0));
print(Math.cos(half_pi));
print(Math.cos(pi));

print("=== floor ===");
print(Math.floor());
print(Math.floor(3.4));

print("=== max ===");
print(Math.max());
print(Math.max(4));
print(Math.max(4, 5));

print("=== pow ===");
print(Math.pow());
print(Math.pow(4));
print(Math.pow(4, 3));

print("=== random ===");
print(Math.random());

print("=== round ===");
print(Math.round());
print(Math.round(2.1));
print(Math.round(2.5));
print(Math.round(-2.5));
print(Math.round(-2.1));

print("=== sin ===");
print(Math.sin());
print(Math.sin(0));
print(Math.sin(half_pi));
print(Math.sin(pi));

print("=== sqrt ===");
print(Math.sqrt());
print(Math.sqrt(4));


